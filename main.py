# -------=–∏–º–ø–æ—Ä—Ç –Ω—É–∂–Ω—ã—Ö –±–∏–±–ª–∏–æ—Ç–µ–∫ –∏ —Ñ–∞–π–ª–æ–≤=-------
from pyrogram import Client, filters  # –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è —Ä–∞–±–æ—Ç—ã —Å —é–∑–µ—Ä–±–æ—Ç–æ–º
from time import sleep  # –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –∑–∞–¥–µ—Ä–∂–∫–∏
import eel  # –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è —Å–æ–∑–¥–∞–Ω–∏—è –≤–µ–± –∏–Ω—Ç–µ—Ä—Ñ–µ–π—Å–∞

@eel.expose  # –ø–æ–¥–∫–ª—é—á–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é –∫ –º–æ–¥—É–ª—é eel
def text(text, chats, time, api_id, api_hash):  # —Ñ—É–Ω–∫—Ü–∏—è
    groups_id = chats.split(' ')  # —É–¥–∞–ª—è–µ–º –∏–∑ —Å—Ç—Ä–æ–∫–∏ chats –ø—Ä–æ–±–µ–ª—ã, –ø–æ—Å–ª–µ —á–µ–≥–æ –ø—Ä–µ–≤—Ä–∞—â–∞–µ–º –µ—ë –≤ —Å–ø–∏—Å–æ–∫
    groups_id = "\n".join(groups_id)
    groups_id = groups_id.split('\n')  #

    def removeel(lst, el):  # —Ñ—É–Ω–∫—Ü–∏—è –ø–æ —É–¥–∞–ª–µ–Ω–∏—é –ª–∏—à–Ω–∏—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –∏–∑ —Å–ø–∏—Å–∫–∞
        els=0  # —Å–æ–∑–¥–∞–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é –∫–æ—Ç–æ—Ä–∞—è —Ä–∞–≤–Ω–∞ 0
        for i in range(len(lst)):  # —Ü–∏–∫–ª –ø–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤—É —ç–ª–µ–º–µ–Ω—Ç–æ–≤ –≤ —Å–ø–∏—Å–∫–µ lst (–µ—Å–ª–∏ –≤ lst 5 —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Ü–∏–∫–ª –≤—ã–ø–æ–ª–Ω–∏—Ç—Å—è 5 —Ä–∞–∑)
            ele = lst[i]  # ele —Ä–æ–≤–Ω–æ —ç–ª–µ–º–µ–Ω—Ç—É –∏–∑ —Å–ø–∏—Å–∫–∞ lst –ø–æ –∏–Ω–¥–µ–∫—Å—É i (–Ω–∞–ø—Ä–∏–º–µ—Ä –µ—Å–ª–∏ —Å–ø–∏—Å–æ–∫ ["1", "2", "3"] –∞ i = 1 —Ç–æ ele —Ä–∞–≤–Ω–æ "2" {–≤ —Å–ø–∏—Å–∫–∞—Ö —Å—á–µ—Ç –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å 0 –∞ –Ω–µ 1})
            if ele == el:  # –µ—Å–ª–∏ ele = el —Ç–æ...
                els+=1  # –ø—Ä–∏–±–∞–≤–ª—è–µ–º 1 –∫ els
        for i in range(els):  # –ø–æ–≤—Ç–æ—Ä—è–µ–º —Ü–∏–∫–ª —Å—Ç–æ–ª—å–∫–æ —Ä–∞–∑ —Å–∫–æ–ª—å–∫–æ –æ–∑–Ω–∞—á–∞–µ—Ç els (–Ω–∞–ø—Ä–∏–º–µ—Ä –µ—Å–ª–∏ els = 1 —Ç–æ —Ü–∏–∫–ª —Ä–∞–±–æ—Ç–∞–µ—Ç 1 —Ä–∞–∑)
            lst.remove(el)  # —É–¥–∞–ª—è–µ–º —ç–ª–µ–º–µ–Ω—Ç
        return lst  # –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –Ω–æ–≤—ã–π —Å–ø–∏—Å–æ–∫

    groups_id = removeel(groups_id, "")  # –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é –ø–æ —É–¥–∞–ª–µ–Ω–∏—é –ª–∏—à–Ω–∏—Ö —ç–ª–µ–º–µ–Ω—Ç–æ–≤

    app = Client("my_account", api_id=api_id, api_hash=api_hash)  # –∑–∞–ø–∏—Å—ã–≤–∞–µ–º –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é app —Ç–æ —á—Ç–æ —é–∑–µ—Ä–±–æ—Ç –±—É–¥–µ—Ç –≤—ã–ø–æ–ª–Ω—è—Ç—å –≤—Å–µ –¥–µ–π—Å—Ç–≤–∏—è –æ—Ç –∏–º–µ–Ω–∏ —é–∑–µ—Ä–∞, –∞ —Ç–∞–∫ –∂–µ –ø–µ—Ä–µ–¥–∞–µ–º api_id –∏ api_hash –∫–æ—Ç–æ—Ä—ã–µ –º—ã –æ–±—ä—è–≤–∏–ª–∏ –≤ —Ñ–∞–π–ª–µ settings.py


    # -------=—Ä–∞–±–æ—Ç–∞ —Å —é–∑–µ—Ä–±–æ—Ç–æ–º=-------
    with app:  # –≤—ã–ø–æ–ª–Ω–µ–Ω–∏–µ –¥–µ–π—Å—Ç–≤–∏–π —Å –ø–æ–º–æ—â—å—é —é–∑–µ—Ä–±–æ—Ç–∞
        while True:  # –≤–µ—á–Ω—ã–π —Ü–∏–∫–ª

            for id in groups_id:  # –±–µ—Ä–µ—Ç id –≥—Ä—É–ø–ø –∏–∑ –∏—Ö —Å–ø–∏—Å–∫–∞, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–æ–ø–∏—Å–∞–Ω –≤ —Ñ–∞–π–ª–µ settings.py
                print(groups_id)

                try:  # –∫–æ–¥ –≤–Ω—É—Ç—Ä–∏ try –±—É–¥–µ—Ç –ø—Ä–æ–≤–µ—Ä—è—Ç—å—Å—è –Ω–∞ –æ—à–∏–±–∫–∏
                    app.send_message(id, text)  # –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ (msg) –≤ –Ω—É–∂–Ω—ã–π —á–∞—Ç –ø–æ id
                    sleep(1)  # –∑–∞–¥–µ—Ä–∂–∫–∞
                except Exception as ex:  # –µ—Å–ª–∏ –±—É–¥–µ—Ç –æ—à–∏–±–∫–∞ –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –µ—ë –∏ –≤–ø–∏—Å—ã–≤–∞–µ–º –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é ex
                    print(ex)  # –≤—ã–≤–æ–¥–∏–º –æ—à–∏–±–∫—É

            sleep(float(time))  # –∑–∞–¥–µ—Ä–∂–∫–∞ –æ—Ç–ø—Ä–∞–≤–∫–∏ —Å–æ–æ–±—â–µ–Ω–∏—è –≤ —á–∞—Å (–≤ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π zdr 3600 —Å–µ–∫ - 1 —á–∞—Å)

# –∏–Ω—Ñ—É –Ω–∏–∂–µ –ª–µ–Ω—å –∫–æ–º–º–µ–Ω—Ç–∏—Ç—å ü§°, –∏ –æ–Ω–∞ –Ω–µ —Ç–∞–∫ –≤–∞–∂–Ω–∞
